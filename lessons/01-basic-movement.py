# Lesson 1: Basic Turtle Movement
# Learn how to move the turtle around the screen

import turtle

# Create a turtle
my_turtle = turtle.Turtle()

# Move the turtle forward
my_turtle.forward(100)

# Turn the turtle
my_turtle.right(90)
my_turtle.forward(50)

# Turn left
my_turtle.left(90)
my_turtle.forward(75)

# Move backward
my_turtle.backward(25)

# Keep the window open
input('Press enter to close')